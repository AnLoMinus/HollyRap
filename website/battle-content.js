// Modal Functionality
document.addEventListener("DOMContentLoaded", function () {
  const modal = document.getElementById("battle-modal");
  const btns = document.querySelectorAll(".view-battle-btn");
  const closeBtn = document.querySelector(".close-modal");

  btns.forEach((btn) => {
    btn.addEventListener("click", function () {
      const battleType = this.dataset.battle;
      const battle = battleContent[battleType];

      if (battle) {
        // Update modal content
        document.querySelector(".battle-view h2").textContent = battle.title;

        // Past verses
        const pastVerses = document.querySelector(
          ".battle-side.past .battle-verses"
        );
        pastVerses.innerHTML = battle.past.verses
          .map((verse) => `<div class="verse">${verse}</div>`)
          .join("");

        // Future verses
        const futureVerses = document.querySelector(
          ".battle-side.future .battle-verses"
        );
        futureVerses.innerHTML = battle.future.verses
          .map((verse) => `<div class="verse">${verse}</div>`)
          .join("");

        // Show modal
        modal.style.display = "block";
        document.body.style.overflow = "hidden";
      }
    });
  });

  // Close modal functionality
  closeBtn.addEventListener("click", function () {
    modal.style.display = "none";
    document.body.style.overflow = "auto";
  });

  window.addEventListener("click", function (e) {
    if (e.target === modal) {
      modal.style.display = "none";
      document.body.style.overflow = "auto";
    }
  });

  // Close with Escape key
  document.addEventListener("keydown", function (e) {
    if (e.key === "Escape" && modal.style.display === "block") {
      modal.style.display = "none";
      document.body.style.overflow = "auto";
    }
  });
});
